package com.she.common.model;

/**
 * menu tree를 구성할 base 모델 클래스
 * ※ 직접 메뉴 트리를 구성하지 않고 frontEnd에서 조합해서 만듦
 */
public class MenuTreeBase {

	private String menuId;

	private String menuNm;

	private String menuLvl;

	private String upMenuId;

	private String urlPath;

	private String sortOrder;

	private String treeSortOrder;

	private String menuFullPath;

	public String getMenuId() {
		return menuId;
	}

	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}

	public String getMenuNm() {
		return menuNm;
	}

	public void setMenuNm(String menuNm) {
		this.menuNm = menuNm;
	}

	public String getMenuLvl() {
		return menuLvl;
	}

	public void setMenuLvl(String menuLvl) {
		this.menuLvl = menuLvl;
	}

	public String getUpMenuId() {
		return upMenuId;
	}

	public void setUpMenuId(String upMenuId) {
		this.upMenuId = upMenuId;
	}

	public String getUrlPath() {
		return urlPath;
	}

	public void setUrlPath(String urlPath) {
		this.urlPath = urlPath;
	}

	public String getSortOrder() {
		return sortOrder;
	}

	public void setSortOrder(String sortOrder) {
		this.sortOrder = sortOrder;
	}

	public String getTreeSortOrder() {
		return treeSortOrder;
	}

	public void setTreeSortOrder(String treeSortOrder) {
		this.treeSortOrder = treeSortOrder;
	}

	public String getMenuFullPath() {
		return menuFullPath;
	}

	public void setMenuFullPath(String menuFullPath) {
		this.menuFullPath = menuFullPath;
	}
}
